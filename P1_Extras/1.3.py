ancho = 17
alto = 12.0

primero = ancho / alto
print(f"ancho / alto = {primero} y es de tipo {type(primero)}")

segundo = ancho // 2
print(f"ancho // 2 = {segundo} y es de tipo {type(segundo)}")

tercero = ancho / 2
print(f"ancho / 2 = {tercero} y es de tipo {type(tercero)}")

cuarto = ancho * 2
print(f"ancho * 2 = {cuarto} y es de tipo {type(cuarto)}")

quinto = ancho * alto
print(f"ancho * alto = {quinto} y es de tipo {type(quinto)}")

sexto = (5 + 1) * 3
print(f"(5 + 1) * 3 = {sexto} y es de tipo {type(sexto)}")

septimo = (5 + 1) / 3
print(f"(5 + 1) / 3 = {septimo} y es de tipo {type(septimo)}")